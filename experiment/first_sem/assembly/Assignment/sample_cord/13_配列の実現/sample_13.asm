	.data
v:      .word   3, 2, 1, 4, 5, 8, 17, 12, 10, 5   # 配列の定義
length: .word   10                                # 配列の長さ

.text
.globl main

main:
    la      $t0, v            # 配列の先頭アドレスを$t0にロード
    lw      $t1, length       # 配列の長さを$t1にロード
    li      $t9, 0            # 総和を保持する$t9を0で初期化
    li      $t2, 0            # ループカウンタを0で初期化

loop:
    bge     $t2, $t1, end     # ループカウンタが配列の長さ以上なら終了
    lw      $t3, 0($t0)       # 配列の現在の要素を$t3にロード
    add     $t9, $t9, $t3     # 総和に現在の要素を加算
    addi    $t0, $t0, 4       # 配列の次の要素に進む（1ワード=4バイト）
    addi    $t2, $t2, 1       # ループカウンタをインクリメント
    j       loop              # ループの先頭に戻る

end:
    # ここで$t9に総和が格納されている
    # プログラムの終了（シミュレータによっては必要）
    li      $v0, 10           # プログラム終了のシステムコール
    syscall